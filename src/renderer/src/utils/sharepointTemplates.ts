// ─────────────────────────────────────────
// HELPERS
// ─────────────────────────────────────────

function escapeHtml(text: string): string {
  const div = document.createElement('div')
  div.textContent = text
  return div.innerHTML
}

function calculateDays(startDate: string, endDate: string): number {
  if (!startDate || !endDate) return 0
  const start = new Date(startDate)
  const end = new Date(endDate)
  if (isNaN(start.getTime()) || isNaN(end.getTime())) return 0
  const diffMs = end.getTime() - start.getTime()
  return Math.max(1, Math.ceil(diffMs / (1000 * 60 * 60 * 24)) + 1)
}

// ─────────────────────────────────────────
// TASK TO TEMPLATE DATA CONVERTERS
// ─────────────────────────────────────────

export function taskToTripData(task: Task, personName: string): TripData {
  return {
    name: task.title,
    person: personName,
    startDate: task.dateStart ? new Date(task.dateStart.toDate()).toISOString() : '',
    endDate: task.dateEnd ? new Date(task.dateEnd.toDate()).toISOString() : '',
    description: task.description || task.notes || ''
  }
}

export function taskToVacationData(
  task: Task, 
  personName: string, 
  vacationType: string
): VacationData {
  const startDate = task.dateStart ? new Date(task.dateStart.toDate()) : null
  const endDate = task.dateEnd ? new Date(task.dateEnd.toDate()) : null
  
  let days = 1
  if (startDate && endDate) {
    const diffMs = endDate.getTime() - startDate.getTime()
    days = Math.max(1, Math.ceil(diffMs / (1000 * 60 * 60 * 24)) + 1)
  }
  
  return {
    person: personName,
    type: (vacationType as VacationData['type']) || 'pto',
    days,
    startDate: startDate ? startDate.toISOString() : '',
    endDate: endDate ? endDate.toISOString() : ''
  }
}
