interface TripData {
  name: string
  person: string
  startDate: string
  endDate: string
  description: string
}

interface VacationData {
  person: string
  type: 'sick-day' | 'pto' | 'compensation' | 'birthday'
  days: number
  startDate: string
  endDate: string
}

function formatDate(dateStr: string): string {
  if (!dateStr) return '—'
  const date = new Date(dateStr)
  if (isNaN(date.getTime())) return dateStr
  return date.toLocaleDateString('en-US', {
    weekday: 'long',
    year: 'numeric',
    month: 'long',
    day: 'numeric'
  })
}

function getVacationTypeLabel(type: string): string {
  const labels: Record<string, string> = {
    'sick-day': 'Sick Day',
    'pto': 'PTO (Paid Time Off)',
    'compensation': 'Compensation Day',
    'birthday': 'Birthday'
  }
  return labels[type] || type
}

